# Casting com int
a = int(2.4)
b = int("3")
c = int(1)

print(a)
print(b)
print(c)
print("\n")

# Casting com float 
d = float(253)
e = float(3)
f = float("4.2")
g = float("90")

print(d)
print(e)
print(f)
print(g)
print("\n")

